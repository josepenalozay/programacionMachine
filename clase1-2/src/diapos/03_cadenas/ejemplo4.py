####################
## EXAMPLE: for loops over strings
####################
una_cadena = "abcdefgh"
for indice in range(len(una_cadena)):
   if una_cadena[indice] == 'e':
       print("La cadena contiene la letra 'e'")

for letra in una_cadena:
   if letra == 'e':
       print("La cadena contiene la letra 'e'")
